package main

import "fmt"

func main() {
	nums := []int{1, 2, 4, 1, 2}

	mask := 0

	for _, num := range nums {
		mask ^= num
	}
	//00000001 1 -  1

	//00000010 2 -  1

	//00000011 3 -  2

	//00000100 4 -  1

	//00000101 5 -  2
	//00000110 6 -  2
	//00000111 7 -  3

	//00001000 8 -  1

	//00001001 9 -  2
	//00001010 10 - 2
	//00001100 11 - 2
	//00001101 12 - 3
	//00001110 13 - 3
	//00001011 14 - 3
	//00001111 15 - 4

	//00010000 16 - 1

	fmt.Println(mask)
	fmt.Println(1 ^ 7)
	fmt.Println(16 >> 6)

}
